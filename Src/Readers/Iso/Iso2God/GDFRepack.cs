		private void buildTables(ref GDFDirTable t, string dir)
		{
			string[] directories = Directory.GetDirectories(dir);
			string[] files = Directory.GetFiles(dir);
			t.Size = (uint) ((directories.Length + files.Length) * 0x90);
			if (t.Parent != null)
			{
				t.Parent.Size = t.Size;
			}
			foreach (string str in directories)
			{
				GDFDirEntry entry;
				entry = new GDFDirEntry {
					Name = str.Substring(str.LastIndexOf(@"\") + 1),
					NameLength = (byte)str.Substring(str.LastIndexOf(@"\") + 1).Length,
					Attributes = GDFDirEntryAttrib.Directory,
					SubDir = new GDFDirTable()
				};
				entry.SubDir.Parent = entry;
				entry.Parent = t;
				t.Add(entry);
				this.buildTables(ref entry.SubDir, str);
			}
			foreach (string str2 in files)
			{
				if (!str2.EndsWith("i2g.iso"))
				{
					GDFDirEntry entry2;
					entry2 = new GDFDirEntry {
						Name = str2.Substring(str2.LastIndexOf(@"\") + 1),
						NameLength = (byte)str2.Substring(str2.LastIndexOf(@"\") + 1).Length,
						Attributes = GDFDirEntryAttrib.Normal,
						SubDir = null,
						Parent = t
					};
					try
					{
						uint.TryParse(File.ReadAllText(str2, Encoding.ASCII), out entry2.Size);
					}
					catch (Exception)
					{
						entry2.Size = 0;
						Console.WriteLine("Exception occured when reading file size from disk for {0}", entry2.Name);
					}
					t.Add(entry2);
				}
			}
			this.tables.Add(t);
		}

		private void calcPath(GDFDirTable t, GDFDirEntry e, ref string path)
		{
			if (e != null)
			{
				path = path.Insert(0, @"\" + e.Name);
				if (e.Parent != null)
				{
					this.calcPath(e.Parent, null, ref path);
				}
			}
			else if (t.Parent != null)
			{
				path = path.Insert(0, @"\" + t.Parent.Name);
				if (t.Parent.Parent != null)
				{
					this.calcPath(t.Parent.Parent, null, ref path);
				}
			}
		}

		public void Dispose()
		{
			this.src = null;
			this.root = null;
			this.tables.Clear();
			GC.Collect();
		}

		public void ExtractGDF()
		{
			Queue<GDFStreamEntry> fileSystem = this.src.GetFileSystem(this.src.RootDir);
			while (fileSystem.Count > 0)
			{
				GDFStreamEntry entry = fileSystem.Dequeue();
				string str = entry.Path.Substring(2, entry.Path.Length - 2);
				if (entry.Entry.IsDirectory)
				{
					if (!Directory.Exists(this.path + str))
					{
						Directory.CreateDirectory(this.path + str);
					}
				}
				else
				{
					File.WriteAllText(this.path + str, entry.Entry.Size.ToString());
				}
			}
		}

		public void GenerateGDF()
		{
			this.tables = new List<GDFDirTable>();
			this.root = new GDFDirTable();
			this.buildTables(ref this.root, this.path);
			this.updateTableSectors();
		}

		private uint sizeToSectors(uint size)
		{
			return (uint) Math.Ceiling((double) (((double) size) / ((double) this.src.VolDesc.SectorSize)));
		}

		private void updateTableSectors()
		{
			this.freeSector = 0x24;
			for (int i = this.tables.Count - 1; i >= 0; i--)
			{
				this.tables[i].CalcSize();
				this.tables[i].Sector = this.freeSector;
				if (this.tables[i].Parent != null)
				{
					this.tables[i].Parent.Sector = this.freeSector;
				}
				this.freeSector += this.sizeToSectors(this.tables[i].Size);
			}
			for (int j = this.tables.Count - 1; j >= 0; j--)
			{
				foreach (GDFDirEntry entry in this.tables[j])
				{
					if (!entry.IsDirectory)
					{
						entry.Sector = this.freeSector;
						this.freeSector += this.sizeToSectors(entry.Size);
					}
				}
				this.tables[j].CreateBST();
			}
		}

		public void WriteData(CBinaryWriter bw)
		{
			for (int i = this.tables.Count - 1; i >= 0; i--)
			{
				foreach (GDFDirEntry entry in this.tables[i])
				{
					if (!entry.IsDirectory)
					{
						string path = "";
						this.calcPath(this.tables[i], entry, ref path);
						if (path.StartsWith(@"\"))
						{
							path = path.Remove(0, 1);
						}
						bw.Seek((long) (entry.Sector * this.src.VolDesc.SectorSize), SeekOrigin.Begin);
						this.src.WriteFileToStream(path, bw);
					}
				}
			}
			this.writeGDFsizes(bw);
		}

		public void WriteGDF(CBinaryWriter bw)
		{
			MemoryStream ms = new MemoryStream();
			ms.Seek(0x12000L, SeekOrigin.Begin);
			for (int i = this.tables.Count - 1; i >= 0; i--)
			{
				if (this.tables[i].Size > 0)
				{
					byte[] buffer = this.tables[i].ToByteArray();
					ms.Write(buffer, 0, buffer.Length);
				}
			}
			this.writeGDFheader(ms);
			bw.Seek(0L, SeekOrigin.Begin);
			bw.Write(ms.ToArray());
			ms.Close();
		}

